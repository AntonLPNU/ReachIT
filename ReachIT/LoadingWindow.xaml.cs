    private readonly DispatcherTimer _tipTimer;
    private int _tipIndex;

    public LoadingWindow()
    {
        InitializeComponent();

        _tipIndex = Random.Shared.Next(Tips.Length);
        TipText.Text = Tips[_tipIndex];

        _tipTimer = new DispatcherTimer
        {
            Interval = TimeSpan.FromSeconds(4.5)
        };
        _tipTimer.Tick += (_, _) => ShowNextTip();
        _tipTimer.Start();

        Closed += (_, _) => _tipTimer.Stop();
    }

    public void ShowStatus(string status)
    {
        StatusText.Text = status;
    }

    private void ShowNextTip()
    {
        _tipIndex = (_tipIndex + 1) % Tips.Length;
        TipText.Text = Tips[_tipIndex];
    }
